    vector<int> rightView(Node *root)
    {
       vector<int>right_view;
       queue<Node*>q;
       q.push(root);
       while(!q.empty()){
           int size = q.size();
           
           while(size--){
               Node*curr = q.front();
               if(size == 0) right_view.push_back(curr->data);
               if(curr -> left != NULL) q.push(curr -> left);
               if(curr -> right != NULL) q.push(curr -> right);
               q.pop();
           }
       }
       return right_view;
    }
